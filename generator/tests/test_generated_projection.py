    def _write_executable(self, name, content):
        path = self.bin / name
        path.write_text(content, encoding="utf-8")
        path.chmod(0o755)

    def _set_vcs_mode(self, mode):
        for name in (".jj", ".git"):
            path = self.root / name
            if path.exists():
                if path.is_dir():
                    path.rmdir()
                else:
                    path.unlink()
        if mode is not None:
            (self.root / mode).mkdir()

    def _run(self, environment=None):
        return subprocess.run(
            ["/bin/bash", str(self.script)],
            cwd="/",
            env=environment or self.environment,
            text=True,
            capture_output=True,
            check=False,
        )

    def _tool_call_lines(self):
        if not self.calls.exists():
            return []
        return self.calls.read_text(encoding="utf-8").splitlines()

    def _ambient_call_lines(self):
        if not self.ambient_calls.exists():
            return []
        return self.ambient_calls.read_text(encoding="utf-8").splitlines()

    def _clear_logs(self):
        for path in (self.calls, self.ambient_calls, self.vcs_calls):
            path.unlink(missing_ok=True)

