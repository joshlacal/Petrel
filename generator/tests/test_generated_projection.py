    async def test_expected_mutation_after_final_read_is_detected_before_skip(self):
        expected_payload = EXPECTED_HEADER + "expected\n"
        changed_payload = EXPECTED_HEADER + "changed after read\n"
        self.root.mkdir(parents=True)
        target = self.root / "Expected.swift"
        target.write_text(expected_payload, encoding="utf-8")
