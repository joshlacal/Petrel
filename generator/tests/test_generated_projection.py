    async def _publish(self, expected_files, output_root=None):
        self.assertIsNotNone(
            projection,
            "generator.generated_projection closed-projection primitive is missing",
        )
        await projection.write_swift_projection(
            str(output_root or self.root),
            expected_files,
        )

    @staticmethod
    def _snapshot(root):
        if not root.exists():
            return None
        snapshot = []
        for path in sorted(root.rglob("*")):
            relative = path.relative_to(root).as_posix()
            if path.is_symlink():
                snapshot.append((relative, ("symlink", path.readlink().as_posix())))
            elif path.is_dir():
                snapshot.append((relative + "/", None))
            else:
                snapshot.append((relative, path.read_bytes()))
        return tuple(snapshot)

