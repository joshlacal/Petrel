        expected_reads = 0
        mutated = False

        def mutate_immediately_after_final_read(parent_fd, name, expected, context):
            nonlocal expected_reads, mutated
            content = original_read(parent_fd, name, expected, context)
            if context == "expected target Expected.swift":
                expected_reads += 1
                if expected_reads == 2:
                    file_fd = os.open(
                        name,
                        os.O_WRONLY | os.O_TRUNC,
                        dir_fd=parent_fd,
                    )
                    try:
                        os.write(file_fd, changed_payload.encode("utf-8"))
                    finally:
                        os.close(file_fd)
                    mutated = True
            return content

        with mock.patch.object(
            projection,
            "_read_regular_file_at",
            side_effect=mutate_immediately_after_final_read,
        ):
            with self.assertRaisesRegex(ValueError, "content|snapshot|changed|identity"):
                await self._publish({"Expected.swift": "expected\n"})

        self.assertTrue(mutated)
        self.assertEqual(target.read_text(encoding="utf-8"), changed_payload)

