def _segment_executables(tokens):
    index = 0
    while index < len(tokens) and tokens[index] in {
        "!",
        "do",
        "elif",
        "else",
        "if",
        "then",
        "time",
        "until",
        "while",
        "{",
    }:
        index += 1
    while index < len(tokens):
        while index < len(tokens) and _SHELL_ASSIGNMENT.match(tokens[index]):
            index += 1
        if index >= len(tokens):
            return ()
        executable = pathlib.PurePosixPath(tokens[index]).name
        if executable == "env":
            index += 1
            while index < len(tokens):
                option = tokens[index]
                if option in {"-u", "--unset"}:
                    index += 2
                elif option.startswith("--unset="):
                    index += 1
                elif option == "--":
                    index += 1
                    break
                elif option.startswith("-"):
                    index += 1
                else:
                    break
            continue
        if executable in {"command", "sudo"}:
            index += 1
            while index < len(tokens) and tokens[index].startswith("-"):
                index += 1
            continue
        results = [executable]
        arguments = tokens[index + 1:]
        if executable in {"bash", "sh", "zsh"}:
            for argument_index, argument in enumerate(arguments):
                if argument.startswith("-") and "c" in argument[1:]:
                    if argument_index + 1 < len(arguments):
                        results.extend(
                            command_position_executables(arguments[argument_index + 1])
                        )
                    break
        elif executable == "eval" and arguments:
            results.extend(command_position_executables(" ".join(arguments)))
        elif executable == "xargs":
            argument_index = 0
            options_with_values = {
                "-E",
                "-I",
                "-L",
                "-P",
                "-n",
                "-s",
                "--max-args",
                "--max-chars",
                "--max-lines",
                "--max-procs",
                "--replace",
            }
            while argument_index < len(arguments):
                argument = arguments[argument_index]
                if argument in options_with_values:
                    argument_index += 2
                elif argument.startswith("-"):
                    argument_index += 1
                else:
                    break
            if argument_index < len(arguments):
                results.extend(_segment_executables(arguments[argument_index:]))
        return tuple(results)
    return ()


def _strip_command_substitutions(text):
    sanitized = []
    substitutions = []
    index = 0
    quote = None
    escaped = False
    while index < len(text):
        character = text[index]
        if escaped:
            sanitized.append(character)
            escaped = False
            index += 1
            continue
        if character == "\\" and quote != "'":
            sanitized.append(character)
            escaped = True
            index += 1
            continue
        if character == "'" and quote != '"':
            quote = None if quote == "'" else "'"
            sanitized.append(character)
            index += 1
            continue
        if character == '"' and quote != "'":
            quote = None if quote == '"' else '"'
            sanitized.append(character)
            index += 1
            continue
        if (
            character == "$"
            and index + 1 < len(text)
            and text[index + 1] == "("
            and quote != "'"
        ):
            inner_start = index + 2
            cursor = inner_start
            depth = 1
            inner_quote = None
            inner_escaped = False
            while cursor < len(text):
                inner_character = text[cursor]
                if inner_escaped:
                    inner_escaped = False
                elif inner_character == "\\" and inner_quote != "'":
                    inner_escaped = True
                elif inner_character == "'" and inner_quote != '"':
                    inner_quote = None if inner_quote == "'" else "'"
                elif inner_character == '"' and inner_quote != "'":
                    inner_quote = None if inner_quote == '"' else '"'
                elif inner_quote is None and inner_character == "(":
                    depth += 1
                elif inner_quote is None and inner_character == ")":
                    depth -= 1
                    if depth == 0:
                        break
                cursor += 1
            if depth != 0:
                raise ValueError("unterminated command substitution")
            substitutions.append(text[inner_start:cursor])
            sanitized.append(" ")
            index = cursor + 1
            continue
        sanitized.append(character)
        index += 1
    return "".join(sanitized), substitutions


def command_position_executables(line):
    """Return every shell-segment executable using a quote-aware lexer."""
    stripped = line.strip()
    if stripped.startswith("run:"):
        stripped = stripped.removeprefix("run:").lstrip()
    try:
        stripped, substitutions = _strip_command_substitutions(stripped)
        lexer = shlex.shlex(
            stripped,
            posix=True,
            punctuation_chars="();&|",
        )
        lexer.commenters = "#"
        lexer.whitespace_split = True
        tokens = list(lexer)
    except ValueError:
        return ()

    segments = []
    current = []
    for token in tokens:
        if token and set(token) <= set("();&|"):
            if current:
                segments.append(current)
                current = []
            continue
        current.append(token)
    if current:
        segments.append(current)
    executables = []
    for segment in segments:
        executables.extend(_segment_executables(segment))
    for substitution in substitutions:
        executables.extend(command_position_executables(substitution))
    return tuple(executables)


def workflow_ambient_executables(line):
    """Conservatively find ambient tool tokens anywhere shell could dispatch them.

    These fixed workflows need no inert prose containing lowercase ambient tool names,
    so scanning every bare token is safer than pretending to fully interpret shell.
    The only legitimate bare tool words are pinned arguments to approved executables.
    Arithmetic expansion is data, not command dispatch, and is removed first.
    """
    policy_text = _ARITHMETIC_EXPANSION.sub(" ", line)
    policy_text = policy_text.replace("--language swift", "--language approved-language")
    policy_text = policy_text.replace(
        "--executable swiftformat",
        "--executable approved-formatter",
    )
    policy_text = policy_text.replace(
        "$GITHUB_WORKSPACE/.build/release-generator-venv/bin/python",
        "$GITHUB_WORKSPACE/.build/release-generator-venv/bin/approved-python",
    )
    violations = [
        match.group(1)
        for match in _FORBIDDEN_AMBIENT_TOKEN.finditer(policy_text)
    ]
    violations.extend(
        f"dynamic:{match.group(1)}"
        for match in _DYNAMIC_SHELL_DISPATCHER_TOKEN.finditer(policy_text)
    )
    for executable in command_position_executables(policy_text):
        if executable in _FORBIDDEN_AMBIENT_EXECUTABLES:
            violations.append(executable)
        elif executable.startswith("$") and executable not in _APPROVED_DYNAMIC_EXECUTABLES:
            violations.append(f"dynamic:{executable}")
    return tuple(dict.fromkeys(violations))


def workflow_run_lines(text):
    """Yield only shell source nested below YAML ``run:`` block scalars."""
    lines = text.splitlines()
    index = 0
    while index < len(lines):
        line = lines[index]
        if line.strip() not in {"run: |", "run: >"}:
            index += 1
            continue
        indentation = len(line) - len(line.lstrip())
        index += 1
        while index < len(lines):
            candidate = lines[index]
            candidate_indentation = len(candidate) - len(candidate.lstrip())
            if candidate.strip() and candidate_indentation <= indentation:
                break
            if candidate.strip():
                yield candidate.strip()
            index += 1


def has_one_isolated_setup_python_action(text):
    lines = text.splitlines()
    action_indices = [
        index
        for index, line in enumerate(lines)
        if re.match(r"^\s*- uses: actions/setup-python@", line)
    ]
    if len(action_indices) != 1:
        return False
    action_index = action_indices[0]
    action_line = lines[action_index]
    if action_line.strip() != "- uses: actions/setup-python@v5":
        return False
    indentation = len(action_line) - len(action_line.lstrip())
    block = []
    for line in lines[action_index + 1:]:
        if line.strip() and len(line) - len(line.lstrip()) <= indentation:
            break
        if line.strip():
            block.append((len(line) - len(line.lstrip()), line.strip()))
    return block == [
        (indentation + 2, "id: release-python"),
        (indentation + 2, "with:"),
        (indentation + 4, "python-version: '3.12'"),
        (indentation + 4, "update-environment: false"),
    ]

